        <p>Ciência da Computação, Sistemas de Informação ou Engenharia da Computação? Qualquer semelhança "não é" mera coincidência. Os 3 cursos de bacharelado em Tecnologia da Informação licenciados pelo Ministério da Educação têm nomes tão similares que até rimam. Mais do que isso, aproximadamente 30% do conteúdo é praticamente o mesmo.
Fábio Miranda, coordenador do curso de Engenharia da Computação no SENAC explica que "os 3 cursos têm um pouco da formação baseada em matemática, lógica de programação e o começo do conhecimento de como criar programas de computador. Então, são habilidades que os 3 vão ter". Apesar da similaridade, os cursos são, sim, bastante diferentes. O problema é saber distinguir. Cerca de 20% dos alunos que ingressam em um desses cursos acaba mudando de um para outro ainda no 1º ano de estudo. Mas então como saber o que escolher na hora de prestar o vestibular?
Para ajudar a vida daqueles que querem seguir carreira em TI, a gente explica um pouco mais.
Na Ciência da Computação, o curso é mais focado em programação e computação. O aluno terá uma sólida formação em áreas como estruturas de dados, algoritmos, linguagens de programação, desenvolvimento e análise de sistemas. O profissional cientista é aquele que vai resolver problemas reais através da computação. Normalmente, os formandos em Ciência da Computação seguem carreiras ligadas ao desenvolvimento de softwares, mas outros também acabam trabalhando com segurança da informação ou estrutura de redes. O profissional formado em Sistemas de Informação é aquele que vai aplicar os recursos da computação para resolver problemas de negócios, especialmente, de atividades corporativas. O curso é mais focado no planejamento e desenvolvimento de sistemas de informação e automação do que necessariamente na programação de softwares.
Já a Engenharia da Computação, o mais completo dos cursos, é diferenciada por se destacar no projeto, desenvolvimento e implementação de equipamentos e dispositivos ligados à computação. Em geral, o engenheiro da computação vai trabalhar mais com hardware do que com o software, ao contrário do que acontece nas outras disciplinas.
Fábio diz que o engenheiro de computação é um profissional que fica no meio termo entre a parte física, ou seja, circuitos eletrônicos e hardware, e a programação: "Então, ele também tem uma formação em programação, mas ele é capaz de construir um computador, por exemplo", diz. Independentemente do curso escolhido, a área de Tecnologia da Informação está super aquecida. Hoje, existem entre 200 e 300 mil vagas de trabalho no setor. E estudos apontam que em 2014, ano de Copa de Mundo por aqui, serão cerca de 800 mil vagas disponíveis para esses profissionais.
Agora, se você quer entrar no mercado de trabalho mais rapidamente, há uma outra categoria chamada de "cursos tecnólogos". Aqui, a formação é mais básica, mas, por outro lado, em 2 ou 3 anos, o profissional está pronto para o mercado de trabalho. Se você quiser saber mais e conhecer melhor esses cursos de curta duração, acesse o link que está junto dessa matéria e confira. Nós explicamos o que são esses cursos e quantas possibilidades existem em tecnologia. Quer você também fazer parte desse time? Vai lá, acesse e boa sorte!

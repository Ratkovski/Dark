        <p>Olá queridos leitores, é como muito prazer que estreamos nosso espaço no CBSI, vamos abordar vários assuntos de interesse de voces na área de TI.
O profissional do século 21? - Hoje em dia uma criança pode usar celular e computador facilmente, mas quando se pergunta o que quer ser quando crescer, pode responder tudo menos que quer ser um profissional de TI. Não é verdade? faça o teste. A bem dizer eu mesmo, quando era criança gostaria de ser dentista, mas fui fisgado aos 11 pelo tal de computador e agradeço ao meu avó por isso.
A falta de incentivo as crianças - Mas porque uma criança apesar de "atacada" diariamente por tecnologia, não gostaria de ser um profissional dessa área? lhe respondo! ainda se deve a uma cultura que a TI é uma profissão "sem futuro", que "paga mal" e trabalha muito. Outro fator é a falta de incentivo lá na infância/adolescência, assim como eu tive de meu avó, a gente vê muito pouco ou quase nada de ensino da informática nos ensinos fundamental e médio, e para quem não sabe existe a Licenciatura em Informática/Computação que forma justamente professores para nossa área. E porque o Brasil não adota essa matéria como obrigatória para nosso jovens? infelizmente meus amigos(as) é falta de visão de nossos "queridos" representantes em Brasília e em sua cidade.
A falta de regulamentação e leis específicas - Sindicatos, associações são ótimos instrumentos para brigar por melhores condições de trabalho para a gente, mas só atende um público extremamente limitado de profissionais. Por isso defendo a criação de conselhos federais e regionais de informática, uma luta antiga que sofreu uma grande derrota ano passado (2014) ao ter seu projeto de lei arquivado pelo senado, nem se quer uma pauta séria, uma lei para incentivo ou proteção dos profissionais de TI, é falta de visão de nossos "queridos" representantes em Brasília e em sua cidade parte 2.
Visão "esdrúxula" da TI reflete nas empresas que comentem os mesmo erros - Com certeza voce já viu que 9 em cada 10 RHs de empresas parecem desconhecer o que a gente faz efetivamente, abrindo uma vaga para um simples programador, mas com exigências de formação fora do comum, e para completar pagam bem menos (É verdade que temos empresas sérias sim, e que fazem exigências e pagamentos dentro das expectativas). Ou seja, as empresas ainda não sabem a real importância da TI para sua estratégia, só vão saber quando falir literalmente.
O crescimento da área é enorme, mas do profissional não, por que? - Não tem pra ninguém, não tem para administração, engenharia, advocacia, medicina. A TI é a área que mais cresce mundialmente, só no Brasil são previstos gastos de 125 bilhões de dólares ( 375 bilhões de reais ) em 2015, crescimento acima do PIB brasileiro, está tudo bem obrigado. Porém quando chega na parte dos profissionais de TI, o crescimento não se reflete a mesma maravilha, com salários estagnados e exigências aumentando o que para mim é uma vergonha.
Chegou a hora de ser reconhecido - A TI não é uma "mau área" pelo contrário, seu defeito e ainda conter pessoas sem visão de futuro, aquelas que ainda nos consideram "menino do computador". Precisamos de ações, para haver reações, e finalmente sermos profissionais vistos e não invisíveis. Começando pela educação que é a base de tudo, há países que já adotaram informática como matéria obrigatória na sala de aula, como Inglaterra e Portugal por exemplo. Na área da política em tempos de eleição exigir de quem voce vai votar, projetos voltados para tecnologia, o que ainda é quase zero.
Apontando agora para o mercado, qualificação é essencial sim, e nossa profissão exige um forte ensino técnico e em idiomas sim senhor, mas voce não é obrigado a aceitar qualquer salário pequeno e horas de trabalho fora do normal. Se valorize, procure saber antes quais áreas de TI são valorizadas na sua região, para se qualificar nela. E já passou da hora de nossos gestores de RH entrar no século 21, e saber o que cada profissional de TI faz, e as empresas serem justas pagando salário de acordo com o mercado, é nossas horas que faz falta a regulamentação que citei, iria separar o joio do trigo.
Por fim acredito que para haver uma mudança de cultura na TI, tem que partir de cada um de nós também, chega de somente reclamar, já sabemos quais os problemas, somos profissionais lógicos e treinados para resolver esses tais problemas. E estamos resolvendo os dos outros e enriquecendo eles, mas esquecemos de resolver a de nós mesmos e enriquecer a nossa profissão tão importante para o século 21, 22, 23 e assim por diante. Incentive, falem bem da TI, mostre seus pontos positivos. #MudeAVisão
